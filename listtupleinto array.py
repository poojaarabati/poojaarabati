import numpy as np

# Sample list and tuple
sample_list = [1, 2, 3, 4, 5]
sample_tuple = (6, 7, 8, 9, 10)

# Convert list to numpy array
list_array = np.array(sample_list)
print("Original List:", sample_list)
print("Array from List:", list_array)

# Convert tuple to numpy array
tuple_array = np.array(sample_tuple)
print("\nOriginal Tuple:", sample_tuple)
print("Array from Tuple:", tuple_array)

